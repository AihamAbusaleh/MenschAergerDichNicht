package de.htwg.menschaergerdichnicht.view;

public interface IMyPoint {
	/**
	 * to get x of the point
	 * 
	 * @return the value of x
	 */
	public int getX();

	/**
	 * to set x of the point
	 */
	public void setX(int x);

	/**
	 * to get y of the point
	 * 
	 * @return the value of y
	 */
	public int getY();

	/**
	 * to set x of the point
	 */
	public void setY(int y);

	/**
	 * to get index of the point
	 * 
	 * @return the value of the index
	 */
	public int getIdx();

	/**
	 * to set index of the point
	 */
	public void setIdx(int idx);

}
